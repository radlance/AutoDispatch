CREATE TABLE request
(
    id                INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_by_id     INT          NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    status_id         INT          NOT NULL REFERENCES request_status (id) ON DELETE RESTRICT,
    origin            VARCHAR(255) NOT NULL,
    destination       VARCHAR(255) NOT NULL,
    loading_point     VARCHAR(255) NOT NULL,
    unloading_point   VARCHAR(255) NOT NULL,
    cargo_type_id     INT REFERENCES cargo_type (id),
    cargo_weight      FLOAT        NOT NULL,
    cargo_volume      FLOAT        NOT NULL,
    cargo_description TEXT,
    trip_date         DATE         NOT NULL,
    customer_id       INT REFERENCES customer (id),
    started_trip_at   TIMESTAMP,
    ended_trip_at     TIMESTAMP,
    created_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);